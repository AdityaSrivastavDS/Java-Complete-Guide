class ifelse {
    public static void main(String[] args) {
        int x = 10;
        if (x < 20) {
            System.out.println("x is smaller than 20");
        }
        else if (x > 20) {
            System.out.println("x is greater than 20");
        }
        else {
            System.out.println("Invalid input");
        }
    }
    
}
