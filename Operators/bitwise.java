class bitwise {
    public static void main(String args[]){
        int a = 10;
        int b = 13;
        
        System.out.println(a & b); // & operator is used to perform bitwise and operation

        System.out.println(a | b); // | operator is used to perform bitwise or operation

        System.out.println(a ^ b); // ^ operator is used to perform bitwise xor operation

        System.out.println(~a); // ~ operator is used to perform bitwise not operation

        System.out.println(a << 2); // << operator is used to perform bitwise left shift operation

        System.out.println(a >> 2); // >> operator is used to perform bitwise right shift operation
    }
    
}
