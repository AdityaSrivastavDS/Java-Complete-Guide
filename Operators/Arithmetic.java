
class Arithmetic {
    public static void main(String args[]){
        int a = 10;
        int b = 25;

        //arithmetic operators
        int su = a+b;  //addition
        int sub = a-b;  //subtraction
        int mul = a*b;  //multiplication
        int div = a/b;  //division
        int mod = a%b;  //modulus

        System.out.println("The sum of a and b is: "+su);
        System.out.println("The difference of a and b is: "+sub);
        System.out.println("The product of a and b is: "+mul);
        System.out.println("The quotient of a and b is: "+div);
        System.out.println("The remainder of a and b is: "+mod);

    }
    
}