class logical {
    public static void main(String args[]){
        int a = 10;
        int b = 20;
        System.out.println(a == b && a > b);  //&& or and operator is used to check if both the conditions are true
        System.out.println(a != b || a > b);   //|| or or operator is used to check if any one of the condition is true
        System.out.println(!(a > b));      //! or not operator is used to check if the condition is false
    } 
    
}
