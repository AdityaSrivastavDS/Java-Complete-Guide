package String;

class first_way{
    public static void main(String args[]){

        //declaration of string using char data type
        //new keyword is used to allocate memory at runtime
        
        char[] ch = new char[5];

        //defining characters of string
        ch[0] = 'H';
        ch[1] = 'e';
        ch[2] = 'l';
        ch[3] = 'l';
        ch[4] = 'o';

        //printing string
        System.out.println(ch);
    }
}
