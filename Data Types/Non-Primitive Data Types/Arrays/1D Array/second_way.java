class second_way{
    public static void main(String args[]){

         //second way to create array
        //we directly declare the elements of value at the time of array creation only
        int ar[] = {6,4,9,2};

        //accessing array
        for(int j=0;j<=ar.length;j++){
            System.out.println(ar[j]);
        }
    }
}

       